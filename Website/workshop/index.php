<h3>Workshop Overview</h3>

<p>
The goal of the workshop is to bring together GUS users and developers in a
setting where users can learn and address issues, and developers can receive
feedback from users and plan for future releases of GUS.  There will be an
emphasis on getting real work done-- installing GUS, running plugins, building websites using the WDK,
and upgrading to 3.5 (which will be released prior to the workshop).
</p>

<p>
The Workshop will be held at the Center for Bioinformatics (PCBI) 
at the University of Pennsylvania in Philadelphia.
</p>

<p>
<strong>Call for Presentations</strong>  If you're interested in speaking at the Workshop, please send a 
brief overview of your talk to <a href="mailto:gusworkshop@pcbi.upenn.edu">gusworkshop@pcbi.upenn.edu</a> by June 10th.
</p>

<p>
<strong>For Questions and More Information</strong>  email <a href="mailto:gusworkshop@pcbi.upenn.edu">gusworkshop@pcbi.upenn.edu</a>.
</p>

