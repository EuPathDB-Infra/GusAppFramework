<h2>Gus Schema, Schema Browser releases</h2>
<p>GusSchema 3.5.1, a patch release is now available from the subversion repository for use with the latest install and GusAppFramework packages.  Upgrade scripts and instructions are included.  Also, a stand-alone SchemaBrowser written by Conrad Ibanez and collaborators at the University of Georgia is now available <a href="http://www.gusdb.org/download">for download</a>.</p>

<h2>Schema Browser 2.0 Released - August 24, 2005</h2>
<p>The first version of the next generation Schema Browser has been released, and is available at <a href="/SchemaBrowser">http://www.gusdb.org/SchemaBrowser</a>.  This release features tables organized by categories, sorting, filtering, and community-submitted documentation (you must have a Wiki username & password to submit documentation).  Any issues should be filed in the <a href="https://www.cbil.upenn.edu/tracker/enter_bug.cgi?product=SchemaBrowser">tracker</a>.
</p>

<h2>GUS 3.5 Released - June 23, 2005</h2>
<p>We're very pleased to announce the release of GUS 3.5.  The 3.5 release includes the introduction of
proteomics support to the schema, PostgreSQL database compatibility, improved documentation, and significant 
additional schema and application layer fixes and enhancements.  For the first time, GUS is available as a 
single distributable from the downloads area on this site.  Upgrade scripts will be available in the coming weeks for
sites interested in moving from GUS 3.0 to 3.5 (note:  The GUS 3.5 application framework is not compatible with previous
schema releases).</p>

<h2>GUS Workshop Details - June 20, 2005</h2>
<p>A 3 day workshop will be held at the Center for Bioinformatics (PCBI) at the University of Pennsylvania in Philadelphia July 6-8, 2005.  Additional details are available on the <a href="/workshop">workshop website</a>. <a href="/workshop/registration.php">Registration</a> is now open for the workshop.</p>

